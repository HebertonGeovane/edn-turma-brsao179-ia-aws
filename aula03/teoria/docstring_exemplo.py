def subtrair(a, b):
    """
    Subtrai o segundo número do primeiro.

    Parâmetros:
    a -- o primeiro número
    b -- o segundo número

    Retorna:
    A diferença entre a e b.
    """
    return a - b

# Usando a função
resultado = subtrair(10, 4)
print("Resultado da subtração:", resultado)

# Acessando a documentação da função
help(subtrair)
